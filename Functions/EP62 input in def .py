# การรับค่าเข้ามาที่ function
# use parameter
def myData(name,lname,age): 
    print("สวัสดี ", name , " ",lname," ",age)

# main
myData("jojo",10,500)  # ส่งไปที่ def

# Argument => ค่าที่ถูกส่งเข้าไปใน function => "jojo",10,500
# Parameter => ตัวที่รับเข้ามูล ที่ถูกส่งมาจาก Argument => (name,lname,age)

