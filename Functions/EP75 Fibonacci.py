# ฟีโบนัชชี่นัมเบอร์
"""
0, 1, (หลักที่1+หลักที่2), (หลักที่2+หลักที่3)  => เอาเลข2ตัวหน้ามารวมกันเพื่อหาค่าปัจจุบัน
0 1 1 2 3 5 8
"""
def febo(num):
    if num <= 1:
        #เลข 2 ลำดับแรก
        return num
    else :
        #เลขลำดับถัดไป
        return febo(num-1) + febo(num-2)  # เอาตัว ก่อนหน้า2ตัวมารวมกัน

for i in range(5): # 0 1 2 3 
    print(febo(i)) # 0 1 1 2 3

